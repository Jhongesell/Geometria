#include "Geometria_1D.hpp"


// Regresa la coordenada del elemento
// @param el Elemento
// @param x Coordenada
void Geometria_1D::coordenadasElemento(int el, ldouble *x)
{
   int i, nd;
   for (i = 0; i < NNE; i++)
   {
      nd = EL->retorna(el-1,i);
      x[i] = ND->retorna(0,nd);
   }
}




